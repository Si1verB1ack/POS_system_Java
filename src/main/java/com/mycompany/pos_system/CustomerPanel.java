    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        if(
            txtName.getText().equals("")||
            txtAge.getValue().equals(0)||
            txtContact.getText().equals("")||
            txtAddress.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please Fill in the information");
        }else{
            Connection con = null;
            PreparedStatement stmt = null;
            ResultSet rs = null;
            try {
                if(btnAdd.getText().equals("Add")){
                    String name = txtName.getText().trim();
                    int age = (int)txtAge.getValue();
                    String gender = (String) cboGender.getSelectedItem();
                    String contact = txtContact.getText();
                    String address = txtAddress.getText();
                    
                    
                    con = DB_Connection.getConnection();
                    stmt=con.prepareStatement("INSERT tblcustomer "
                            + "(customer_name, age, gender, contact, address) "
                            + "VALUES (?,?,?,?,?);");
                    
                    stmt.setString(1, name);
                    stmt.setInt(2, age);
                    stmt.setString(3, gender);
                    stmt.setString(4, contact);
                    stmt.setString(5, address);
                    
                    stmt.execute();
                    
                    model.setRowCount(0);

                    showData();

                    if (model.getRowCount() > 0) {
                        jtProductList.scrollRectToVisible(jtProductList.getCellRect(model.getRowCount() - 1, 0, true));
                    }

                    JOptionPane.showMessageDialog(this, "Customer has been added successfully");
                    clear();
                }else{
                    UpdateCustomer();
                    }
            } catch (SQLException ex) {
            ex.printStackTrace(); // Handle exception properly, maybe log it or show an error message
            } finally {
                try {
                    if (rs != null) rs.close();
                    if (stmt != null) stmt.close();
                    if (con != null) con.close();
                } catch (SQLException ex) {
                    ex.printStackTrace(); // Handle exception properly, maybe log it or show an error message
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

