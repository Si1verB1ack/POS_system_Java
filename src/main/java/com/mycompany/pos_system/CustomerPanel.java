    private void UpdateCustomer() {
    int selectedIndex = jtProductList.getSelectedRow();
    if(selectedIndex != -1){
        Connection con = null;
        PreparedStatement stmt = null;  
        ResultSet rs = null;
        
        int CustomerID =Integer.parseInt(jtProductList.getValueAt(selectedIndex, 0).toString());
        
        String name = txtName.getText().trim();
        int age = (int)txtAge.getValue();
        String gender = cboGender.getSelectedItem().toString();
        String contact = txtContact.getText();
        String address = txtAddress.getText();
        
        try {
            
             con = DB_Connection.getConnection();
            
            stmt = con.prepareStatement("UPDATE tblcustomer SET customer_name=?, age=? , gender=? , contact=?, address=?  WHERE customer_id=?");

            stmt.setString(1, name);
            stmt.setInt(2, age);
            stmt.setString(3, gender);
            stmt.setString(4, contact);
            stmt.setString(5, address);
            stmt.setInt(6, CustomerID);// You need to replace 'productId' with the actual product ID you want to update

            
            stmt.executeUpdate();
            model.setRowCount(0);
            showData();
            if (model.getRowCount() > 0) {
                        jtProductList.scrollRectToVisible(jtProductList.getCellRect(model.getRowCount() - 1, 0, true));
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // Handle exception properly, maybe log it or show an error message
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (con != null) con.close();
            } catch (SQLException ex) {
                ex.printStackTrace(); // Handle exception properly, maybe log it or show an error message
            }
        }

        clear();
        
        JOptionPane.showMessageDialog(this, "Customer has been updated successfully");
        }
    }
